package com.androidadvanced.petfinder.models;

public class Stats {
    private int seen;
    private int helping;

    public Stats() {}

    public int getSeen() {
        return seen;
    }

    public void setSeen(int seen) {
        this.seen = seen;
    }

    public int getHelping() {
        return helping;
    }

    public void setHelping(int helping) {
        this.helping = helping;
    }
}
